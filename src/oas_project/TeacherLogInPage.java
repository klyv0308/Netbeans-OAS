    private void btnLogInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogInActionPerformed

        String username = txtUsername.getText();
        String password = String.valueOf(passwordField.getPassword());

        String dbUrl = "jdbc:mysql://localhost:3306/oas_database";
        String dbUser = "root";
        String dbPass = "mysql-DB1502";

        try (Connection conn = DriverManager.getConnection(dbUrl, dbUser, dbPass)) {
            String query = """
            SELECT teacher_data FROM teacher_subjects
            WHERE username = ? AND teacher_password = ?
            LIMIT 1
            """;

            PreparedStatement stmt = conn.prepareStatement(query);
            stmt.setString(1, username);
            stmt.setString(2, password); // <-- Fails if password is null or query is wrong

            ResultSet rs = stmt.executeQuery();

            if (rs.next()) {
                String teacherPassword = rs.getString("teacher_password");
                // ðŸ” Open the StudentAttendanceLog window
                new StudentAttendanceLog(teacherPassword);
                this.dispose(); // close the login window
            } else {
                JOptionPane.showMessageDialog(this, "Invalid username or password.", "Login Failed", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Database error:\n" + e.getMessage());
        }
    }//GEN-LAST:event_btnLogInActionPerformed

